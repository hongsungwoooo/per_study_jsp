package com.withpill.web.user.dto;

public class ProductDTO {
	
	private String productCode;
	private String productName;
	private int productQuentity;
	private String productPrice;
	private int productSoldout;
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public int getProductQuentity() {
		return productQuentity;
	}
	public void setProductQuentity(int productQuentity) {
		this.productQuentity = productQuentity;
	}
	public String getProductPrice() {
		return productPrice;
	}
	public void setProductPrice(String productPrice) {
		this.productPrice = productPrice;
	}
	public int getProductSoldout() {
		return productSoldout;
	}
	public void setProductSoldout(int productSoldout) {
		this.productSoldout = productSoldout;
	}
	
	

}
